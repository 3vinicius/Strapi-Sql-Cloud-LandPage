function mapData(data = {}) {
  const {
    menu = {},
    sections = [],
    title = '',
    slug = '',
    footer: footerHtml = '',
  } = data;

  return {
    footerHtml,
    slug,
    title,
    sections: mapSections(sections),
    menu: mapLink(menu),
  };
}
function mapLink(menuDate = {}) {
  const {
    open_in_new_tab: newTab = false,
    logo_text: text = '',
    logo: { url: srcImg } = '', //no have url in logo
    logo_link: link = '',
    menu_links: links = [],
  } = menuDate;
  return {
    newTab,
    text,
    srcImg,
    link,
    links: mapMenuLinks(links),
  };
}

const mapMenuLinks = (links = []) => {
  return links.map((value) => {
    const {
      open_in_new_tab: newTab = false,
      url: link = '',
      link_text: children = '',
    } = value;
    return {
      newTab,
      children,
      link,
    };
  });
};

function mapSections(sections = []) {
  return sections.map((section) => {
    if (section.__component === 'section.section-two-columns') {
      return mapSectionTwoColumns(section);
    }
    if (section.__component === 'section.section-content') {
      return mapSectionContent(section);
    }
    if (section.__component === 'section.section-grid') {
      const { text_grid = [], image_grid = [{}] } = section;
      if (text_grid.length > 0) {
        return mapTextGrid(section);
      }
      if (image_grid.length > 0) {
        return mapImageGrid(section);
      }
      return { ...section, text_grid, image_grid };
    }
    return section;
  });
}

const mapSectionTwoColumns = (section = {}) => {
  const {
    __component: component = '',
    title = '',
    description: text = '',
    metadata: { background = false, section_id: sectionId = '' } = false,
    image: {
      data: {
        attributes: {
          formats: {
            medium: { url: srcImg },
          },
        },
      },
    } = '',
  } = section;
  return {
    component,
    title,
    text,
    background,
    sectionId,
    srcImg,
  };
};

const mapSectionContent = (section = {}) => {
  const {
    __component: component = '',
    title = '',
    content: html = '',
    metadata: { background = false, section_id: sectionId = '' } = false,
  } = section;
  return {
    component,
    title,
    html,
    background,
    sectionId,
  };
};

const mapTextGrid = (section = {}) => {
  const {
    title = '',
    description = '',
    metadata: { background = false, section_id: sectionId = '' } = false,
    text_grid: grid = [{}],
  } = section;
  return {
    component: 'section.section-grid-text',
    title,
    description,
    background,
    sectionId,
    grid: grid.map((text) => {
      const { title = '', description = '' } = text;
      return {
        title,
        description,
      };
    }),
  };
};

const mapImageGrid = (section) => {
  const {
    title = '',
    description = '',
    metadata: { background = false, section_id: sectionId = '' } = false,
  } = section;
  const grid = section.image_grid[0].images.data;
  console.log(grid);

  return {
    component: 'section.section-grid-image',
    title,
    description,
    background,
    sectionId,
    grid: grid.map((img) => {
      const {
        attributes: {
          formats: {
            medium: { url: srcImg },
          },
          altText = '',
        } = '',
      } = img;

      return {
        srcImg,
        altText,
      };
    }),
  };
};

